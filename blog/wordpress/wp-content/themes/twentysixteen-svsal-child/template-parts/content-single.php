			) );
		?>
	</div><!-- .entry-content -->

	<footer class="entry-footer">
		<div class="tags">
			<?php twentysixteen_entry_meta2(); ?>
		</div>

		<?php if ( function_exists( 'get_author_bio_box' ) ) echo get_author_bio_box(); ?>

		<?php echo do_shortcode('[ssba]'); ?>

